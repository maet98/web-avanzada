package edu.pucmm.severless.models;

import com.amazonaws.services.dynamodbv2.datamodeling.*;

import java.time.LocalDateTime;

@DynamoDBTable(tableName = "reservation")
public class Reservation {

    @DynamoDBHashKey(attributeName = "reservationId")
    @DynamoDBAutoGeneratedKey
    private String reservationId;

    @DynamoDBAttribute(attributeName = "id")
    private String Id;

    @DynamoDBAttribute(attributeName = "nombre")
    private String nombre;

    @DynamoDBAttribute(attributeName = "carrera")
    private String carrera;

    @DynamoDBAttribute(attributeName = "laboratorio")
    private String laboratorio;

    @DynamoDBAttribute(attributeName = "fechaReserva")
    private LocalDateTime fechaReserva;

    public Reservation(String reservationId, String id, String nombre, String carrera, String laboratorio, LocalDateTime fechaReserva) {
        this.reservationId = reservationId;
        Id = id;
        this.nombre = nombre;
        this.carrera = carrera;
        this.laboratorio = laboratorio;
        this.fechaReserva = fechaReserva;
    }

    public String getReservationId() {
        return reservationId;
    }

    public void setReservationId(String reservationId) {
        this.reservationId = reservationId;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCarrera() {
        return carrera;
    }

    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }

    public String getLaboratorio() {
        return laboratorio;
    }

    public void setLaboratorio(String laboratorio) {
        this.laboratorio = laboratorio;
    }

    public LocalDateTime getFechaReserva() {
        return fechaReserva;
    }

    public void setFechaReserva(LocalDateTime fechaReserva) {
        this.fechaReserva = fechaReserva;
    }
}
